/// Belt animation: get the animated glyph for an empty belt based on frame counter.
pub fn belt_animated_glyph(belt_type: EntityType, facing: Facing, frame: u32) -> char {
    match belt_type {
        EntityType::BasicBelt => {
            if (frame / 4) % 2 == 0 {
                '\u{00B7}'
            } else {
                match facing {
                    Facing::Right => '\u{203A}',
                    Facing::Left => '\u{2039}',
                    Facing::Up => '\u{02C4}',
                    Facing::Down => '\u{02C5}',
                }
            }
        }
        EntityType::FastBelt => {
            if (frame / 2) % 2 == 0 {
                '\u{00B7}'
            } else {
                match facing {
                    Facing::Right => '\u{00BB}',
                    Facing::Left => '\u{00AB}',
                    Facing::Up => '\u{02C4}',
                    Facing::Down => '\u{02C5}',
                }
            }
        }
        EntityType::ExpressBelt => {
            if frame % 2 == 0 {
                '\u{00B7}'
            } else {
                match facing {
                    Facing::Right => '\u{21D2}',
                    Facing::Left => '\u{21D0}',
                    Facing::Up => '\u{21D1}',
                    Facing::Down => '\u{21D3}',
                }
            }
        }
        _ => '\u{00B7}',
    }
}

